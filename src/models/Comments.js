import { Schema, models, model } from "mongoose"

const CommentSchema = new Schema(
  {
    fullname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Comments = models.Comments || model("Comments", CommentSchema);

export default Comments;
